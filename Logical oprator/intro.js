// Logical Operators
// (|| - OR), ( && - AND), !

const name = "bob";
const age = 34;
if (name === "amul" || age === 34) {
  console.log("hello there ");
} else {
  console.log("wrong value");
}

if (name === "amul" && age === 34) {
  console.log("hello there ");
} else {
  console.log("wrong value");
}
