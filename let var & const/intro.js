// LET VS CONST VS VAR
// using var
var value = "some value";
value = " some other value";
// using let
let name = "amul";
name = "atul";
// using const (constant) - can't re-assign
const lastName = "patle";

console.log(value);
console.log(name);
console.log(lastName);

const about = "hey my name is amul's patle";
console.log(about);
